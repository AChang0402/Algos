function isValidSudoku(board){
    for (let i=0; i<9; i++){
        let seen = {}
        for (let j=0; j<9; j++){
            if(board[i][j] !== "."){
                if(seen.hasOwnProperty(board[i][j])){
                    return false
                }
                else{
                    seen[board[i][j]] = "seen"
                }
            }
        }
    }

    for (let j=0; j<9; j++){
        let seen = {}
        for (let i=0; i<9; i++){
            if(board[i][j] !== "."){
                if(seen.hasOwnProperty(board[i][j])){
                    return false
                }
                else{
                    seen[board[i][j]] = "seen"
                }
            }
        }
    }

    let seen = {}
    for (let multiply = 0; multiply<3; multiply++){
        for (let i=0; i<9;i++){
            for (let j=0; j<3; j++){
                if(i%3 == 0 && j%3 == 0){
                    seen = {}
                }
                if(board[i][(multiply)*3+j]!== "."){
                    if(seen.hasOwnProperty(board[i][(multiply)*3+j])){
                        return false
                    }
                    else{
                        seen[board[i][(multiply)*3+j]] = "seen"
                    }
                }
            }
        }
    }
    return true
}

